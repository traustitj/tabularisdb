import { readFileSync, writeFileSync } from "fs";
import { resolve } from "path";

const paths = {
  sponsorsTs: resolve("website/src/lib/sponsors.ts"),
  sponsorsMd: resolve("SPONSORS.md"),
  readme: resolve("README.md"),
};

// --- Parse sponsors.ts ---
const tsSource = readFileSync(paths.sponsorsTs, "utf-8");

// Strip TypeScript-specific syntax so the file can be eval'd as JS
const jsSource = tsSource
  .replace(/export interface \w+\s*\{[\s\S]*?\n\}/g, "")
  .replace(/export const SPONSORS:\s*\w+\[\]\s*=/, "const SPONSORS =");

let sponsors;
try {
  sponsors = new Function(`${jsSource}; return SPONSORS;`)();
} catch (e) {
  console.error("❌ Failed to parse sponsors.ts:", e.message);
  process.exit(1);
}

console.log(`🔄 Syncing ${sponsors.length} sponsors...`);

// --- Generate SPONSORS.md ---
const lines = [
  "# Sponsors",
  "",
  "Tabularis is made possible thanks to the support of our sponsors.",
  "Interested in sponsoring? [Get in touch →](https://tabularis.dev/sponsors)",
  "",
  "---",
  "",
];

for (const s of sponsors) {
  lines.push(`## ${s.name}`);
  lines.push("");

  if (s.logoImg) {
    const logoPath = `website/public${s.logoImg}`;
    const bg = s.logoImgBg ? ` style="background:${s.logoImgBg};padding:6px;"` : "";
    lines.push(
      `<a href="${s.url}" target="_blank"><img src="${logoPath}" height="40" alt="${s.name}"${bg} /></a>`,
    );
    lines.push("");
  }

  lines.push(`**${s.tagline}**`);
  lines.push("");
  lines.push(`🔗 [${s.url}](${s.url})`);
  lines.push("");

  if (s.features && s.features.length > 0) {
    for (const f of s.features) {
      lines.push(`- ${f.icon} ${f.text}`);
    }
    lines.push("");
  }

  if (s.offer) {
    lines.push(`> **${s.offer.title}**`);
    lines.push(`> ${s.offer.description}`);
    lines.push("");
  }

  lines.push("---");
  lines.push("");
}

lines.push(
  "_This file is auto-generated by `scripts/sync-sponsors.js`. Do not edit manually._",
);

writeFileSync(paths.sponsorsMd, lines.join("\n"), "utf-8");
console.log("✅ Generated SPONSORS.md");

// --- Update README.md ---
const START = "<!-- SPONSORS:START -->";
const END = "<!-- SPONSORS:END -->";

const readmeBlock = [
  START,
  "",
  "## Sponsors",
  "",
  ...sponsors.map((s) => {
    const logo = s.logoImgCompact ?? s.logoImg;
    const imgTag = logo
      ? `<img src="website/public${logo}" height="28" alt="${s.name}" /> `
      : "";
    return `- ${imgTag}**[${s.name}](${s.url})** — ${s.tagline}`;
  }),
  "",
  "_[Become a sponsor →](https://tabularis.dev/sponsors)_",
  "",
  END,
].join("\n");

let readme = readFileSync(paths.readme, "utf-8");

if (readme.includes(START) && readme.includes(END)) {
  readme = readme.replace(new RegExp(`${START}[\\s\\S]*?${END}`), readmeBlock);
} else {
  // Insert before ## License
  readme = readme.replace("## License", `${readmeBlock}\n\n## License`);
}

writeFileSync(paths.readme, readme, "utf-8");
console.log("✅ Updated README.md");
